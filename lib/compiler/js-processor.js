'use strict';

/**
 * JS Processor for the DYWO compiler pipeline.
 *
 * Transforms JavaScript from .dywo <script> blocks into webpack-compatible
 * module code. Handles import extraction, alias rewriting, and entry module
 * generation.
 */

const path = require('path');

// Regex that matches all ES `import … from '…'` forms:
//   import DefaultExport from '...'
//   import { named } from '...'
//   import * as ns from '...'
//   import DefaultExport, { named } from '...'
const IMPORT_RE = /import\s+(?:\w+|\{[^}]+\}|\*\s+as\s+\w+|\w+\s*,\s*(?:\{[^}]+\}|\*\s+as\s+\w+))\s+from\s+['"]([^'"]+)['"]/g;

// Matches bare `import '...'` / `import "..."` side-effect imports
const SIDE_EFFECT_IMPORT_RE = /import\s+['"]([^'"]+)['"]/g;

// Matches `export default class ClassName {` or `export default {`
const EXPORT_DEFAULT_RE = /export\s+default\s+(?:class\s+\w+|{)/;

const JSProcessor = {
  /**
   * Transform a .dywo <script> block into webpack-compatible module code.
   *
   * @param {string} scriptContent  Raw content of the <script> block.
   * @param {string} filePath       Absolute path to the .dywo file (for alias resolution).
   * @param {object} [options]      Optional options (aliases, warn, etc.).
   * @returns {Promise<{ code: string, imports: string[], hasDefaultExport: boolean }>}
   */
  async transform(scriptContent, filePath, options = {}) {
    const aliases = options.aliases || {};
    const warn = typeof options.warn === 'function' ? options.warn : msg => process.stderr.write('[dywo warn] ' + msg + '\n');

    // If script is empty/missing, synthesise a minimal component.
    if (!scriptContent || scriptContent.trim() === '') {
      const name = _componentNameFromPath(filePath);
      return {
        code: `export default { name: '${name}' };\n`,
        imports: [],
        hasDefaultExport: true
      };
    }

    // Rewrite @ aliases and collect imports.
    const rewritten = JSProcessor.rewriteImports(scriptContent, filePath, aliases);
    const imports = JSProcessor.extractImports(rewritten);

    const hasDefaultExport = EXPORT_DEFAULT_RE.test(rewritten);

    if (!hasDefaultExport) {
      warn(`No 'export default' found in ${filePath}. The component may not render correctly.`);
    }

    return {
      code: rewritten,
      imports,
      hasDefaultExport
    };
  },

  /**
   * Extract all import specifiers (the string inside `from '...'`) from a
   * script block. Includes both named/default imports and bare side-effect
   * imports.
   *
   * @param {string} scriptContent
   * @returns {string[]}  De-duplicated list of import specifiers.
   */
  extractImports(scriptContent) {
    const specifiers = new Set();

    // Named / default / namespace imports
    let match;
    const importRe = new RegExp(IMPORT_RE.source, 'g');
    while ((match = importRe.exec(scriptContent)) !== null) {
      specifiers.add(match[1]);
    }

    // Side-effect imports (`import './foo'`)
    const sideEffectRe = new RegExp(SIDE_EFFECT_IMPORT_RE.source, 'g');
    while ((match = sideEffectRe.exec(scriptContent)) !== null) {
      specifiers.add(match[1]);
    }

    return Array.from(specifiers);
  },

  /**
   * Rewrite import specifiers: resolves `@` path aliases to their real
   * paths while leaving relative paths (including .dywo imports) as-is for
   * webpack / dywo-loader to handle.
   *
   * @param {string} scriptContent
   * @param {string} filePath       Absolute path to the containing .dywo file.
   * @param {object} [aliases]      Map of alias prefix → absolute directory.
   *                                e.g. { '@': '/project/src' }
   * @returns {string}  Script content with aliases replaced.
   */
  rewriteImports(scriptContent, filePath, aliases = {}) {
    if (!aliases || Object.keys(aliases).length === 0) {
      return scriptContent;
    }

    const fileDir = path.dirname(filePath);

    // Replace both `import … from` and side-effect `import` specifiers.
    return scriptContent.replace(/(\bfrom\s+|^import\s+)(['"])([^'"]+)\2/gm, (match, prefix, quote, specifier) => {
      const resolved = _resolveAlias(specifier, aliases, fileDir);
      if (resolved === specifier) return match; // nothing changed
      return `${prefix}${quote}${resolved}${quote}`;
    });
  },

  /**
   * Generate the webpack entry module string for a .dywo root component.
   *
   * The runtime is expected to be loaded separately (e.g. via a <script> tag
   * or a separate webpack chunk). The generated entry registers the root
   * component with the runtime when it is available, or queues it for later.
   *
   * @param {string} componentPath  Path to the root .dywo component, as it
   *                                should appear in the import statement.
   * @param {object} [config]       Optional config (el selector, etc.).
   * @returns {string}
   */
  generateEntry(componentPath, config = {}) {
    const el = (config && config.el) || '#app';

    return `// Auto-generated by DYWO — do not edit
import App from '${componentPath}';

// Register the root component with the DYWO runtime. The runtime's auto-init
// (Dywo._init) reads window.__DYWO_APP__ on DOMContentLoaded and mounts it.
if (typeof window !== 'undefined') {
  window.__DYWO_APP__ = { component: App, el: '${el}' };
}
`;
  }
};

