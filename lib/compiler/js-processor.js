/**
 * Derive a PascalCase component name from a file path.
 * e.g. '/project/src/MyButton.dywo' → 'MyButton'
 * @param {string} filePath
 * @returns {string}
 */
function _componentNameFromPath(filePath) {
  if (!filePath) return 'Component';
  const base = path.basename(filePath, path.extname(filePath));
  // Convert kebab-case / snake_case to PascalCase
  return base
    .split(/[-_]/)
    .map(part => part.charAt(0).toUpperCase() + part.slice(1))
    .join('');
}

/**
 * Resolve a single import specifier against the known alias map.
 * Returns the original specifier if no alias matched.
 *
 * @param {string} specifier   e.g. '@/components/Foo.dywo'
 * @param {object} aliases     e.g. { '@': '/abs/path/to/src' }
 * @param {string} fileDir     Absolute directory of the importing file.
 * @returns {string}           Relative path or original specifier.
 */
function _resolveAlias(specifier, aliases, fileDir) {
  for (const [alias, targetDir] of Object.entries(aliases)) {
    // Match `@/…`, `@pages/…`, etc. The alias key may or may not end with `/`.
    const prefix = alias.endsWith('/') ? alias : alias + '/';
    if (specifier === alias || specifier.startsWith(prefix)) {
      const remainder = specifier.slice(prefix.length);
      const absoluteTarget = path.join(targetDir, remainder);
      let rel = path.relative(fileDir, absoluteTarget).replace(/\\/g, '/');
      // Ensure the path starts with './' so webpack treats it as relative.
      if (!rel.startsWith('.')) rel = './' + rel;
      return rel;
    }
  }
  return specifier;
}

module.exports = JSProcessor;
